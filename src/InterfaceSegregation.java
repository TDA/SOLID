public class InterfaceSegregation {
    // Interface segregation means that interfaces should basically have high cohesion, i.e. they
    // should only contain methods that are relevant to their clients, formally stated, it says that
    // no clients should be forced to implement methods that are not relevant to them. If interfaces
    // have a lot fo methods, they should be broken down into smaller, more cohesive components (in this
    // way, it is very similar to single responsibility).
}
