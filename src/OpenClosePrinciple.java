public class OpenClosePrinciple {
    // This states that classes should be open for extension, but closed for modification
    // basically it means we can extend a class by inheritance, but not modify it, i.e. the classes
    // should act more like interfaces, and we can then extend their functionality without having to
    // modify the original class
}
